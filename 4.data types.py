# Numeric data types
#integer
x=5
result=type(x)
print(result)

#float
x=5.5
print(type(x))

#Complex
x=2+3j
result=type(x)
print(result)

#bool
x=5
y=9
result=type(x<y)
print(result)

# Sequence data types
#string
x='Nasir'
print(type(x))

#list
x=[3,4,5,6]
result=type(x)
print(result)

#tuple
x=(2,3,4,5)
result=type(x)
print(result)

# set 
x={2,3,4,5}
print(type(x))